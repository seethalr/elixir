package com.seethalreghunath.elixir;

public class Medicine {
    public String medicine;
    public String totalcount;
    public String countperday;
    public String duration;
    public String time1;
    public String time2;
    public String time3;
    public String time4;


    public Medicine(String medicine, String totalcount, String countperday, String duration, String time1, String time2, String time3, String time4) {
        this.medicine = medicine;
        this.totalcount = totalcount;
        this.countperday = countperday;
        this.duration = duration;
        this.time1 = time1;
        this.time2 = time2;
        this.time3 = time3;
        this.time4 = time4;
    }
}
